import acm.program.*;

public class FindRange extends ConsoleProgram {
	
	private static final int SENTINEL = 0;
	
	public void run() {
		while (true) {
			int value = readInt(" ? ");
			if (value == SENTINEL) break;
		}
		
		
	}
}

